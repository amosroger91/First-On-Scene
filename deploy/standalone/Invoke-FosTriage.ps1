<#
.SYNOPSIS
    First-On-Scene - SINGLE-FILE incident triage for a possibly-compromised Windows host.
.DESCRIPTION
    Self-contained. No dependencies, no internet, no other files required. Paste it into a
    NinjaOne / ScreenConnect / RMM run-command window (or run locally), and it will:
      * collect volatile + persistent forensic artifacts (READ-ONLY),
      * inventory remote-access / RMM tools and flag any you did NOT authorize,
      * decide a deterministic verdict (ALL CLEAR / MONITOR / PROBLEM DETECTED),
      * seal the evidence (SHA-256 manifest + tamper-evident chain of custody),
      * print a clear verdict to the console and write an HTML report.

    Tell it which remote-access tools are EXPECTED so it only flags the rest:
        Invoke-FosTriage.ps1 -ExpectedRemoteTools 'NinjaOne,ScreenConnect'

    Exit codes (for RMM): 0 ALL_CLEAR | 10 MONITOR | 20 PROBLEM (Incident) | 21 PROBLEM (Breach) | 1 error
.NOTES
    Engine logic mirrors the modular scripts; the embedded ruleset is generated from
    rules/detections.json by tools/Build-Standalone.ps1. Run as Administrator for full coverage.
