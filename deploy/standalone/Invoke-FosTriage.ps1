        try{ $pfDir="$env:windir\Prefetch"; if(Test-Path $pfDir){Get-ChildItem -LiteralPath $pfDir -Filter *.pf -ErrorAction SilentlyContinue|Sort-Object LastWriteTimeUtc -Descending|Select-Object -First 300|ForEach-Object{[void]$prefetch.Add([ordered]@{name=$_.Name;lastRunUtc=$_.LastWriteTimeUtc.ToString('o')})}} }catch{CErr 'prefetch' $_.Exception.Message}
        try{ $modCache=@{}; foreach($p in (Get-Process -ErrorAction SilentlyContinue)){ try{ foreach($m in $p.Modules){ $mp=$m.FileName; if($mp -match '\\(Users|Temp|AppData|ProgramData)\\'){ if(-not $modCache.ContainsKey($mp)){$stt='';try{$stt=[string](Get-AuthenticodeSignature -LiteralPath $mp -ErrorAction SilentlyContinue).Status}catch{};$modCache[$mp]=$stt}; if($modCache[$mp] -ne 'Valid'){[void]$injectedModules.Add([ordered]@{process=$p.ProcessName;module=$mp;signatureStatus=$modCache[$mp]})} } } }catch{} } }catch{CErr 'injectedModules' $_.Exception.Message}
    }

    # host / system info (context - never affects the verdict)
    $cs=$null
    $sysInfo=[ordered]@{}
    try{
        $cs=Get-CimInstance Win32_ComputerSystem -ErrorAction SilentlyContinue
        $bios=Get-CimInstance Win32_BIOS -ErrorAction SilentlyContinue
        $os=Get-CimInstance Win32_OperatingSystem -ErrorAction SilentlyContinue
        $cpu=@(Get-CimInstance Win32_Processor -ErrorAction SilentlyContinue)|Select-Object -First 1
        $lastBoot='';$uptime=''
        if($os -and $os.LastBootUpTime){ try{ $lb=$os.LastBootUpTime; $lastBoot=$lb.ToUniversalTime().ToString('o'); $sp=(Get-Date)-$lb; $uptime=('{0}d {1}h {2}m' -f $sp.Days,$sp.Hours,$sp.Minutes) }catch{} }
        $sysInfo=[ordered]@{
            manufacturer=[string]$cs.Manufacturer; model=[string]$cs.Model; serialNumber=[string]$bios.SerialNumber
            systemType=[string]$cs.SystemType; domain=[string]$cs.Domain; partOfDomain=[bool]$cs.PartOfDomain
            osCaption=[string]$os.Caption; osVersion=[string]$os.Version; osBuild=[string]$os.BuildNumber; osArchitecture=[string]$os.OSArchitecture
            cpu=[string]$cpu.Name; cpuCores=[int]$cpu.NumberOfCores; cpuLogical=[int]$cpu.NumberOfLogicalProcessors
            ramGB=$(if($cs.TotalPhysicalMemory){[math]::Round([double]$cs.TotalPhysicalMemory/1GB,1)}else{0}); lastBootUtc=$lastBoot; uptime=$uptime
        }
    }catch{CErr 'systemInfo' $_.Exception.Message}

    # fixed disks (capacity / free space)
    $disks=New-Object System.Collections.ArrayList
    try{ foreach($d in (Get-CimInstance Win32_LogicalDisk -Filter 'DriveType=3' -ErrorAction SilentlyContinue)){ $sz=[double]$d.Size; $fr=[double]$d.FreeSpace; [void]$disks.Add([ordered]@{drive=[string]$d.DeviceID;volumeName=[string]$d.VolumeName;sizeGB=[math]::Round($sz/1GB,1);freeGB=[math]::Round($fr/1GB,1);usedGB=[math]::Round(($sz-$fr)/1GB,1);percentFree=$(if($sz -gt 0){[math]::Round(($fr/$sz)*100,0)}else{0})}) } }catch{CErr 'disks' $_.Exception.Message}

    # logged-on interactive users
    $loggedOnUsers=New-Object System.Collections.ArrayList
    try{
        $seen=@{}
        foreach($e in (Get-CimInstance Win32_Process -Filter "Name='explorer.exe'" -ErrorAction SilentlyContinue)){
            try{ $o=Invoke-CimMethod -InputObject $e -MethodName GetOwner -ErrorAction SilentlyContinue
                if($o -and $o.User){ $u=("{0}\{1}" -f $o.Domain,$o.User).TrimStart('\'); $k=$u.ToLower(); if(-not $seen.ContainsKey($k)){$seen[$k]=$true;[void]$loggedOnUsers.Add([ordered]@{user=$u;session='interactive';source='explorer'})} } }catch{}
        }
        if($cs -and $cs.UserName){ $u=[string]$cs.UserName; $k=$u.ToLower(); if(-not $seen.ContainsKey($k)){$seen[$k]=$true;[void]$loggedOnUsers.Add([ordered]@{user=$u;session='console';source='ComputerSystem'})} }
    }catch{CErr 'loggedOnUsers' $_.Exception.Message}

    $bundle=[ordered]@{
        schemaVersion='3.0.0'
        metadata=[ordered]@{caseId=$caseId;collectionTimestampUtc=(Fos-Utc);targetHostname=$env:COMPUTERNAME;platform='windows';osVersion=(Get-CimInstance Win32_OperatingSystem -ErrorAction SilentlyContinue).Caption;collectorVersion=$FosVersion;executionMode='local';evidenceIntegrityMode=$true;expectedRemoteTools=@($expectedTokens);operator=(Fos-Operator);host=[ordered]@{system=$sysInfo;disks=@($disks);loggedOnUsers=@($loggedOnUsers)};errors=@($errors)}
        artifacts=[ordered]@{
            remoteAccess=[ordered]@{tools=@($remoteTools)}
            securityPosture=[ordered]@{defender=$defenderPosture}
            accessControl=$accessControl
            defenseEvasion=[ordered]@{logClearEvents=@($logClear)}
            persistence=[ordered]@{registryRunKeys=@($runKeys);scheduledTasks=@($tasks);services=@($services);wmiEventSubscriptions=$wmi;asep=@($asep)}
            execution=[ordered]@{processes=@($processes);processCreationEvents=@();prefetch=@($prefetch);injectedModules=@($injectedModules)}
            network=[ordered]@{connections=@($connections)}
            credentialAccess=[ordered]@{logonEvents=@($logonEvents);privilegeEscalationEvents=@($privEsc);userCreationEvents=@($userCreate);serviceInstallEvents=@($svcInstall)}
            fileSystem=[ordered]@{fileMetadata=@($fileMeta);browserArtifacts=@()}
            powerShellActivity=[ordered]@{scriptBlockLogs=@($psLogs)}
            antivirusScans=[ordered]@{defenderScan=$defender;clamavScan=[ordered]@{executed=$false;threatsFound=0;threats=@()}}
        }
    }
    $bundlePath=Join-Path $CaseDir 'bundle.json'; Fos-WriteJson $bundle $bundlePath
    Fos-CocAdd 'COLLECT_DONE' "proc=$($processes.Count) svc=$($services.Count) conn=$($connections.Count) remoteTools=$($remoteTools.Count)" @($bundlePath)
    Fos-Log "Collected. Analyzing..." 'STEP'

    # analyze
    $bundleObj=Get-Content $bundlePath -Raw|ConvertFrom-Json
    $ruleset=$RULES_JSON|ConvertFrom-Json; $thr=$ruleset.scoring.thresholds
    $findings=@()
    foreach($rule in $ruleset.rules){
        $ev=if($rule.type -eq 'builtin'){Invoke-FosBuiltin $bundleObj $rule}else{@(@(Resolve-FosPath $bundleObj $rule.selector)|Where-Object{$_ -and (Test-FosItem $_ $rule)})}
        if(@($ev).Count -gt 0){ $cnt=@($ev).Count; $findings+=[pscustomobject]@{ruleId=$rule.id;name=$rule.name;category=$rule.category;mitre=$rule.mitre;severity=$rule.severity;weight=[int]$rule.weight;rationale=$rule.rationale;falsePositive=$rule.falsePositive;reasonCode=$rule.reasonCode;escalate=[bool]$rule.escalateToBreach;evidenceCount=$cnt;evidence=@($ev|Select-Object -First 25)} }
    }
    $score=0; foreach($f in $findings){ $cap=$MultCap[$f.severity]; if(-not $cap){$cap=1}; $score+=($f.weight*[Math]::Min($f.evidenceCount,$cap)) }
    $verdict='ALL_CLEAR'; if($score -ge [int]$thr.problem){$verdict='PROBLEM_DETECTED'}elseif($score -ge [int]$thr.monitor){$verdict='MONITOR'}
    $isBreach=($score -ge [int]$thr.breach) -or (@($findings|Where-Object{$_.escalate}).Count -gt 0)
    switch($verdict){'ALL_CLEAR'{$classification='None';$exit=0}'MONITOR'{$classification='Event';$exit=10}'PROBLEM_DETECTED'{if($isBreach){$classification='Breach';$exit=21}else{$classification='Incident';$exit=20}}}
    $maxSev='info'; foreach($f in $findings){ if($SeverityRank[$f.severity] -gt $SeverityRank[$maxSev]){$maxSev=$f.severity} }
    $top=$findings|Sort-Object -Property @{Expression={$_.weight*[Math]::Min($_.evidenceCount,3)}} -Descending|Select-Object -First 1
    $reasonCode=if($top){$top.reasonCode}else{'NO_FINDINGS'}

    $result=[ordered]@{caseId=$caseId;analyzedTimestampUtc=(Fos-Utc);engineVersion=$FosVersion;rulesetVersion=$ruleset.rulesetVersion;targetHostname=$env:COMPUTERNAME;verdict=$verdict;score=$score;severity=$maxSev;classification=$classification;reasonCode=$reasonCode;thresholds=$thr;findings=@($findings)}
    $findingsPath=Join-Path $CaseDir 'findings.json'; Fos-WriteJson $result $findingsPath

    # HTML report
    $vc=switch($verdict){'ALL_CLEAR'{'#16a34a'}'MONITOR'{'#d97706'}default{'#dc2626'}}
    $sevColor=@{info='#6b7280';low='#2563eb';medium='#d97706';high='#ea580c';critical='#dc2626'}
    function HE($s){ if($null -eq $s){return ''}; [System.Net.WebUtility]::HtmlEncode([string]$s) }
    $rows=''
    foreach($d in ($findings|Sort-Object -Property @{Expression={$_.weight};Descending=$true})){ $clr=$sevColor[$d.severity]; $m=if($d.mitre){"$($d.mitre.technique)"}else{'-'}; $ej=HE(($d.evidence|ConvertTo-Json -Depth 6 -Compress)); if($ej.Length -gt 1000){$ej=$ej.Substring(0,1000)+' ...'}; $rows+="<tr><td><code>$(HE $d.ruleId)</code></td><td>$(HE $d.name)<div class=note>$(HE $d.rationale)</div></td><td><span class=pill style='background:$clr'>$(HE $d.severity)</span></td><td style=text-align:center>$($d.evidenceCount)</td><td><code>$(HE $m)</code></td></tr><tr><td></td><td colspan=4><details><summary>evidence</summary><pre>$ej</pre></details></td></tr>" }
    if(-not $rows){$rows="<tr><td colspan=5 style='text-align:center;color:#16a34a'>No detections fired.</td></tr>"}
    # Host card (system info + disks + logged-on users + remote-access tools, all informational)
    $hostCard=''
    $hi2=$bundle.metadata.host
    if($hi2){
        $si=$hi2.system
        $domTxt=if($si.partOfDomain){HE $si.domain}else{(HE $si.domain)+' (workgroup)'}
        $diskRows=''
        foreach($dk in @($hi2.disks)){ $pf=[double]$dk.percentFree; $dcol=if($pf -lt 10){'#dc2626'}elseif($pf -lt 20){'#d97706'}else{'#111827'}; $diskRows+="<tr><td><code>$(HE $dk.drive)</code> $(HE $dk.volumeName)</td><td>$($dk.sizeGB) GB</td><td>$($dk.freeGB) GB</td><td style='color:$dcol'>$($dk.percentFree)% free</td></tr>" }
        if(-not $diskRows){$diskRows="<tr><td colspan=4 style='color:#6b7280'>none</td></tr>"}
        $userList=(@($hi2.loggedOnUsers)|ForEach-Object{ (HE $_.user)+' <span class=note>('+(HE $_.session)+')</span>' }) -join ', '
        if(-not $userList){$userList='<span class=note>none detected</span>'}
        $rtools=@($bundle.artifacts.remoteAccess.tools)
        $rtxt=(@($rtools)|ForEach-Object{ $lab=if($_.authorized){'expected'}else{'undeclared'}; (HE $_.tool)+' <span class=note>('+$lab+')</span>' }) -join ', '
        if(-not $rtxt){$rtxt='<span class=note>none detected</span>'}
        $hostCard="<div class=card><h2>Host</h2><div class=kv><b>Machine</b><span>$(HE $si.manufacturer) $(HE $si.model)</span><b>Serial</b><span>$(HE $si.serialNumber)</span><b>OS</b><span>$(HE $si.osCaption) (build $(HE $si.osBuild), $(HE $si.osArchitecture))</span><b>CPU</b><span>$(HE $si.cpu) ($($si.cpuCores)C/$($si.cpuLogical)T)</span><b>RAM</b><span>$($si.ramGB) GB</span><b>Domain</b><span>$domTxt</span><b>Uptime</b><span>$(HE $si.uptime)</span><b>Logged-on users</b><span>$userList</span><b>Remote-access / RMM</b><span>$rtxt</span></div><table style='margin-top:12px'><tr><th>Disk</th><th>Size</th><th>Free</th><th>Capacity</th></tr>$diskRows</table></div>"
    }
    $reportName="Incident_Report_$($env:COMPUTERNAME)_$((Get-Date).ToString('yyyyMMdd_HHmmss')).html"
    $reportPath=Join-Path $CaseDir $reportName
    $html="<!DOCTYPE html><html><head><meta charset='utf-8'><title>$BrandName Incident Report</title><style>body{font-family:Segoe UI,Arial,sans-serif;margin:0;background:#f3f4f6;color:#111827}header{background:#0f172a;color:#fff;padding:16px 24px}.wrap{max-width:1000px;margin:20px auto;padding:0 16px}.card{background:#fff;border:1px solid #e5e7eb;border-radius:10px;padding:16px 20px;margin-bottom:16px}.verdict{font-size:24px;font-weight:700;color:$vc}table{width:100%;border-collapse:collapse;font-size:13px}th,td{padding:7px 9px;border-bottom:1px solid #eef0f3;text-align:left;vertical-align:top}th{background:#f9fafb;font-size:11px;text-transform:uppercase;color:#6b7280}.pill{color:#fff;padding:2px 8px;border-radius:999px;font-size:11px}.note{color:#6b7280;font-size:12px}code{background:#f1f5f9;padding:1px 5px;border-radius:4px}pre{background:#0f172a;color:#e2e8f0;padding:8px;border-radius:6px;overflow:auto;font-size:11px}.kv{display:grid;grid-template-columns:180px 1fr;gap:5px 14px;font-size:14px}</style></head><body><header><h1>$BrandName &middot; Incident Triage Report</h1></header><div class=wrap><div class=card><div class=verdict>$verdict</div><div class=kv><b>Case</b><span>$(HE $caseId)</span><b>Host</b><span>$(HE $env:COMPUTERNAME)</span><b>Classification</b><span>$(HE $classification)</span><b>Severity</b><span>$(HE $maxSev)</span><b>Score</b><span>$score</span><b>Reason</b><span><code>$(HE $reasonCode)</code></span><b>Analyzed (UTC)</b><span>$(HE $result.analyzedTimestampUtc)</span></div></div>$hostCard<div class=card><h2>Detections ($(@($findings).Count))</h2><table><tr><th>Rule</th><th>Finding</th><th>Severity</th><th>Hits</th><th>MITRE</th></tr>$rows</table></div></div><footer style='text-align:center;color:#9ca3af;font-size:12px;padding:18px'>Generated by $BrandName (open source). Deterministic engine - zero network egress.</footer></body></html>"
    Fos-WriteText $html $reportPath
    Fos-CocAdd 'ANALYSIS_DONE' "verdict=$verdict score=$score class=$classification findings=$(@($findings).Count)" @($findingsPath,$reportPath)
    Fos-ManifestNew $caseId

    # ---- console verdict (tech-facing) ----
    if(-not $Quiet){
        $bar=('='*60)
        $vcolor=switch($verdict){'ALL_CLEAR'{'Green'}'MONITOR'{'Yellow'}default{'Red'}}
        Write-Host ""; Write-Host $bar -ForegroundColor $vcolor
        Write-Host ("  {0}  ::  {1}{2}" -f $BrandName.ToUpper(), $verdict, $(if($verdict -eq 'PROBLEM_DETECTED'){" ($classification)"}else{''})) -ForegroundColor $vcolor
        Write-Host ("  HOST: {0}    SCORE: {1}    REASON: {2}" -f $env:COMPUTERNAME,$score,$reasonCode) -ForegroundColor $vcolor
        Write-Host $bar -ForegroundColor $vcolor
        $hi=$bundle.metadata.host
        if($hi){
            $si=$hi.system
            Write-Host "  Machine:" -ForegroundColor Gray
            Write-Host ("   {0} {1}   S/N {2}" -f $si.manufacturer,$si.model,$si.serialNumber) -ForegroundColor DarkGray
            Write-Host ("   OS: {0} (build {1}, {2})" -f $si.osCaption,$si.osBuild,$si.osArchitecture) -ForegroundColor DarkGray
            Write-Host ("   CPU: {0}  ({1}C/{2}T)   RAM: {3} GB   Uptime: {4}" -f $si.cpu,$si.cpuCores,$si.cpuLogical,$si.ramGB,$si.uptime) -ForegroundColor DarkGray
            $dom=if($si.partOfDomain){$si.domain}else{"$($si.domain) (workgroup)"}
            Write-Host ("   Domain: {0}" -f $dom) -ForegroundColor DarkGray
            if(@($hi.disks).Count -gt 0){
                Write-Host "  Disks:" -ForegroundColor Gray
                foreach($dk in $hi.disks){ $lowc=if([double]$dk.percentFree -lt 10){'Red'}elseif([double]$dk.percentFree -lt 20){'Yellow'}else{'DarkGray'}; Write-Host ("   {0}  {1} GB free of {2} GB ({3}% free)" -f $dk.drive,$dk.freeGB,$dk.sizeGB,$dk.percentFree) -ForegroundColor $lowc }
            }
            if(@($hi.loggedOnUsers).Count -gt 0){
                Write-Host "  Logged-on users:" -ForegroundColor Gray
                foreach($u in $hi.loggedOnUsers){ Write-Host ("   {0} ({1})" -f $u.user,$u.session) -ForegroundColor DarkGray }
            } else { Write-Host "  Logged-on users: none detected" -ForegroundColor DarkGray }
            Write-Host $bar -ForegroundColor $vcolor
        }
        if(@($findings).Count -gt 0){
            Write-Host "  Top findings:" -ForegroundColor Gray
            foreach($d in ($findings|Where-Object{$_.weight -gt 0}|Sort-Object -Property @{Expression={$_.weight};Descending=$true}|Select-Object -First 8)){
                Write-Host ("   [{0,-8}] {1}  {2}" -f $d.severity,$d.ruleId,$d.name) -ForegroundColor $(switch($d.severity){'critical'{'Red'}'high'{'Red'}'medium'{'Yellow'}default{'Gray'}})
            }
        }
        $rt=@($bundle.artifacts.remoteAccess.tools)
        if($rt.Count -gt 0){
            Write-Host "  Remote-access / RMM tools (informational - does not affect verdict):" -ForegroundColor Gray
            foreach($t in $rt){ $tag=if($t.authorized){'[expected]  '}else{'[undeclared]'}; Write-Host ("   {0} {1} ({2})" -f $tag,$t.tool,$t.evidence) -ForegroundColor $(if($t.authorized){'DarkGray'}else{'Cyan'}) }
            $undef=@($rt|Where-Object{-not $_.authorized})
            if($undef.Count -gt 0 -and -not $ExpectedRemoteTools){ Write-Host "   (tip: pass -ExpectedRemoteTools 'NinjaOne,ScreenConnect' to label your own agents as expected)" -ForegroundColor DarkGray }
            elseif($undef.Count -gt 0){ Write-Host "   (undeclared = not in your -ExpectedRemoteTools list; confirm each is one you installed)" -ForegroundColor DarkGray }
        }
        $dp=$bundle.artifacts.securityPosture.defender
        if($dp -and $dp.available){ $rtp=if($dp.realTimeEnabled){'on'}else{'OFF'}; $tpp=if($dp.tamperProtectionEnabled){'on'}else{'OFF'}; $exC=(@($dp.exclusionPaths)+@($dp.exclusionExtensions)+@($dp.exclusionProcesses)|Where-Object{$_}).Count; Write-Host ("  Defender: real-time={0}  tamper-protection={1}  exclusions={2}" -f $rtp,$tpp,$exC) -ForegroundColor $(if(-not $dp.realTimeEnabled){'Red'}else{'DarkGray'}) }
        Write-Host $bar -ForegroundColor $vcolor
        Write-Host ("  Report:   {0}" -f $reportPath) -ForegroundColor Gray
        Write-Host ("  Evidence: {0}  (sealed: SHA-256 manifest + chain of custody)" -f $bundlePath) -ForegroundColor Gray
        Write-Host $bar -ForegroundColor $vcolor; Write-Host ""
    }
    Fos-Log "DONE. Verdict=$verdict Score=$score Class=$classification" 'OK'
    exit $exit
}
catch {
    Write-Host "FATAL: $($_.Exception.Message)" -ForegroundColor Red
    exit 1
}
